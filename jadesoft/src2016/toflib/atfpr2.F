C   15/02/82 204081220  MEMBER NAME  ATFPR2   (S)           FORTRAN
      SUBROUTINE ATFPR2(IPTOF)
      IMPLICIT INTEGER*2 (H)
      COMMON/TFPED/ HADC(2,42),HTDC(2,42),HSTAT(42),HON(42)
      COMMON/FWPED/ HADCF(2,16),HTDCF(2,16),HFSTAT(16),HFON(16)
      COMMON/TFPED1/HTDC1(2,42),HON1(42),HTSPAR(16)
#include "chead.for"
      DATA IBUG /0/
C
      NRUN = HEADR(18)
      IBUG = IBUG + 1
      PRINT 300,IBUG,NRUN
  300 FORMAT(//' NEW EVENT',2I6/)
      CALL ATOFRW(IPTOF)
      PRINT 305,HON
      PRINT 305,HON1
      PRINT 305,HFON
  305 FORMAT(' HON',42I3)
      DO   1   I=1,42
      IF(I.LE.16.AND.
     +   HON(I).LE.0.AND.HFON(I).LE.0.AND.HON1(I).LE.0)  GOTO  1
      IF(I.GT.16.AND.
     +   HON(I).LE.0.AND.HON1(I).LE.0)  GOTO  1
      PRINT301,I,HADC(1,I),HADC(2,I),HTDC(1,I),HTDC(2,I),HTDC1(1,I),
     +           HTDC1(2,I)
  301 FORMAT(I5,6I6)
      IF(I.GT.16)  GOTO  1
      IF(HFON(I).LE.0)  GOTO 1
      PRINT302,I,HADCF(1,I),HADCF(2,I),HTDCF(1,I),HTDCF(2,I)
  302 FORMAT('+',52X,I5,4I6)
    1 CONTINUE
C
      PRINT304,HTSPAR
  304 FORMAT(/' SPARE TDC"S ',16I5/)
C
      RETURN
      END
