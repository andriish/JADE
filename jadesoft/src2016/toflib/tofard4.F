        SUBROUTINE TOFARD(INLA,INTF,IFLG,*)
C==================================================
C  MODIFIED TO CHECK ON TDCS INSTEAD OF LATCHES
C==================================================
C          CONVERTS THE TOF DATA FROM CAMAC FORMAT
C       TO ANALYSIS FORMAT :
C                   IRAW(1,NCN)= NCN  ; COUNTER#
C      RAW(2,NCN)= TDC(-)-CM(NCN)
C      RAW(3,NCN)= TDC(+)-CP(NCN)
C       OR
C      RAW(2,NCN)= TDC(-) , IF IFLG= 1.
C      RAW(3,NCN)= TDC(+) , IF IFLG= 1.
C                   RAW(4,NCN) = ADC(-)
C                   RAW(5,NCN) = ADC(+)
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
CC
#include "tfprm.for"
#include "tfadc.for"
#include "tcalcm1.for"
#include "ddata.for"
      COMMON/POINT/IPHEAD,IPLTCH,IPT1,IPTOF,IPCLST,IPR,IPPATR,IPNT(12),
     1             IPBP
      COMMON/PRPOS/PRPLS,CPOS
      INTEGER *2 HEADR(108)
      COMMON /CHEADR/ IHEADR(54)
      EQUIVALENCE (IHEADR(1),HEADR(1))
      DIMENSION MASK(16),LIST(19)
C
      DATA MASK/1,2,4,8,16,32,64,128,256,512,1024,2048,4096,8192,
     1          16384,32768/
      DATA IENTRY/0/,LERR/0/,IBUG/0/,JBUG/0/
      DATA MBAD/19/
      DATA LIST/5,7,8,32,34,35,36,11,27,28,29,30,31,33,37,38,39,
     1          41,42/
C     DATA MBAD/19/
C     DATA LIST/5,7,40,32,34,35,36,11,27,28,29,30,31,33,37,38,39,41,42/
      DATA ADCLIM/1./
      IF(IENTRY.NE.0)  GOTO 320
      IENTRY = IENTRY + 1
      PRINT 310
  310 FORMAT(80X,'  TOFARDC CALLED')
C
  320 CONTINUE
C
C========= ADDRESSING
C
      NRUN = HEADR(18)
      CPOS = HEADR(31)/1000.
       INLA2= INLA*2
       INTF2= INTF*2
       IL0= INLA2+6
       IA0= INTF2+3
       IT0= INTF2+94
C//////////
      IBUG = IBUG + 1
      BADC = TPARM(1)
      AADC = TPARM(2)
C
      DO 3000 NCN= 1,42
      IRAW(1,NCN) = 0
      HSTAT(NCN) = 0
C
C========= ADDRESS OF ADC AND TDC FOR COUNTER# NCN
C
      NCN2= NCN*2
      IAA= (NCN-1)/6+NCN2+IA0-2
      IAT= (NCN-1)/4+NCN2+IT0-2
      IAA1= IAA+1
      IAT1= IAT+1
      HADC(1,NCN)= HDATA(IAA)
      HADC(2,NCN)= HDATA(IAA1)
      ADCM = HDATA(IAA) - PEDLM(NCN)
      ADCP = HDATA(IAA1) - PEDLP(NCN)
      IF(NRUN.LE.2306.OR.NCN.LE.38)  GOTO  75
      IF(NRUN.GE.3182)  GOTO  75
      ADCM = ADCM*2
      ADCP = ADCP*2
   75 HTDC(1,NCN)= HDATA(IAT)
      HTDC(2,NCN)= HDATA(IAT1)
      IF(NCN.NE.25.AND.NCN.NE.26)  GOTO  76
      IF(NRUN.LT.3728.OR.NRUN.GT.4426)  GOTO  76
      IF(NCN.EQ.25) IAT = IT0-2+58
      IF(NCN.EQ.26) IAT = IT0-2+56
      HTDC(1,NCN)= HDATA(IAT)
   76 CONTINUE
      MONE = 0
      IF(NRUN.LT.7588.OR.NRUN.GT.10000)  GOTO  77
      IF(NRUN.LT.7900)  GOTO  78
      IF(NCN.EQ.40) HTDC(1,NCN)=2050
      IF(NCN.EQ.40) MONE = 2
   78 CONTINUE
      DO   31   L=1,MBAD
      IF(NRUN.GE.8256.AND.L.GT.4)  GOTO  77
      IF(NRUN.GE.7897.AND.L.GT.7)  GOTO  77
C     IF(NRUN.GE.8256.AND.L.GT.3)  GOTO  77
C     IF(NRUN.GE.7897.AND.L.GT.7)  GOTO  77
      IF(NCN.NE.LIST(L))  GOTO  31
      HTDC(2,NCN) = 2050
      MONE = 1
   31 CONTINUE
   77 CONTINUE
      TDCM = HTDC(1,NCN)
      TDCP = HTDC(2,NCN)
C
C========= CHECK THAT BOTH TDC'S HAVE FIRED
C
      HSTAT(NCN) = MONE
      IF(MONE.EQ.2)  GOTO  32
      IF(HTDC(1,NCN).LT. 5.OR.HTDC(1,NCN).GE.2048)  GOTO  3000
      IF(MONE.EQ.1)  GOTO  1500
   32 IF(HTDC(2,NCN).LT. 5.OR.HTDC(2,NCN).GE.2048)  GOTO  3000
C
C========= THEY HAVE
C
 1500 CONTINUE
C
C  COMPUTE PULSEHEIGHT CORRECTION FOR TOF
C
C     IF(NRUN.LE.7900)  GOTO  22
C     IF(NCN.NE.40)  GOTO  22
C     JBUG = JBUG + 1
C     IF(JBUG.LE.2) CALL ATOFPR(IPTOF)
C  22 CONTINUE
      CORADM = 0.
      CORADP = 0.
      IF(HADC(1,NCN).GE.1024) ADCM = 1024.
      IF(HADC(2,NCN).GE.1024) ADCP = 1024.
      IF(ADCM.LE.0.) ADCM = 1.
      IF(ADCP.LE.0.) ADCP = 1.
      ADCMIV = 1000./ADCM
      ADCPIV = 1000./ADCP
      CORADM =  AADC + BADC*ADCMIV
      CORADP =  AADC + BADC*ADCPIV
      IF(NRUN.LT.3728)  GOTO  20
      CORADM =   CADCB(2*NCN-1)*SQRT(ADCMIV)
      CORADP =   CADCB(2*NCN  )*SQRT(ADCPIV)
   20 IF(ADCM.LT.1024.) CMS = CORNOR(2*NCN-1)
      IF(ADCP.LT.1024.) CPS = CORNOR(2*NCN)
      IF(ADCM.GE.1024.) CMS = COROVF(2*NCN-1)
      IF(ADCP.GE.1024.) CPS = COROVF(2*NCN)
C
C     IF(IBUG.LE.10) PRINT 106,NCN,CADCA(2*NCN-1),CADCB(2*NCN-1),
C    1 CADCA(2*NCN),CADCB(2*NCN)
C     IF(IBUG.LE.10) PRINT 106,NCN,ADCM,ADCP,CORADM,CORADP,CMS,CPS
C 106 FORMAT(' TOFARD',I5,10F10.3)
      IF(TDCM.LT.10..OR.TDCM.GT.2048.)  GOTO  2000
      IF(ADCM.LT.ADCLIM)  GOTO  2000
      TDCM0= TDCM
      IF(IFLG.EQ.1) GO TO 1600
      TDCM0= TDCM0*TCALM(NCN)
      TDCM0= TDCM0-CMS-CORADM
C     IF(IBUG.LE.10) PRINT 106,NCN,TDCM0
 1600 IRAW(1,NCN)= NCN
      RAW(2,NCN)= TDCM0
      RAW(4,NCN)= ADCM
      IF(MONE.EQ.1)  GOTO  3000
C
 2000 CONTINUE
      IF(TDCP.LT.10..OR.TDCP.GT.2048.)  GOTO  2100
      IF(ADCP.LT.ADCLIM)  GOTO  2100
      TDCP0= TDCP
      IF(IFLG.EQ.1) GO TO 2050
2010  TDCP0= TDCP0*TCALP(NCN)
      TDCP0= TDCP0-CPS-CORADP
C     IF(IBUG.LE.10) PRINT 106,NCN,TDCP0
2050  IRAW(1,NCN)= NCN
      RAW(3,NCN)= TDCP0
      RAW(5,NCN)= ADCP
2100  CONTINUE
C
 3000 CONTINUE
C/////////
      RETURN
      END
