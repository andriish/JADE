C   13/10/80 010131214  MEMBER NAME  GGFAC6   (S)           FORTRAN
      SUBROUTINE GGFAC6
C---  COPY CALIBRATION CONSTANTS OF LEAD-GLASS INTO COMMON /CALICO/
C---  RUNS 4993 ONWARDS
C
C     H.WRIEDT    13.09.79     19:00
C     LAST MODIFICATION    13.10.80   12:15
C
      COMMON /CFACT6/ FACT6(191)
      COMMON /CALICO/ FAKTOR(191)
C
        DO 1 I = 1,191
    1   FAKTOR(I) = FACT6(I)
      WRITE(6,600)
  600 FORMAT(/' *** FIFTH SET OF LEAD-GLASS CALIBRATION CONSTANTS',
     &        ' INCLUDED ***')
      RETURN
      END
      BLOCK DATA
C
      COMMON /CFACT6/ FACT6
C     THIS DATA APPLICABLE FROM RUN 4993 ONWARDS
C     THE GAIN ASSUMES ENERGY(MEV)=CHANNEL*5.0
      REAL*4 FACT6(191)/191*1.5/
      END
