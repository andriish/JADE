      SUBROUTINE ERRPRT(IRETCD,IERRNO)
C
C     PRINTOUT OF RUN + EVENT # FOR EACH ERROR OCCURRENCE
C     P. STEFFEN 11/03/80
C
      IMPLICIT INTEGER*2 (H)
C
C-------------------------------
C  MACRO CHEADR .... HEADER BANK
C-------------------------------
      COMMON /CHEADR/ IHEADR(54)
      INTEGER*2 HHEADR(108)
      EQUIVALENCE (IHEADR(1),HHEADR(1))
C --
C --  HHEADR(17) = EXPERIMENT NUMBER
C --  HHEADR(18) = RUN NUMBER
C --  HHEADR(19) = EVENT NUMBER
C --  HHEADR(38) = MAGNETIC FIELD (GAUSS)
C --
C--------- END OF MACRO CHEADR ------------
      LOGICAL*1 LBREAD
      COMMON /CADMIN/ IEVTP,NRREAD,NRWRIT,NRERR,LBREAD(4),IFLG
      EQUIVALENCE (HHEADR(18),HRUN) , (HHEADR(19),HEV)
C
  100 FORMAT(' *** ERROR TRAP ***')
      WRITE(6,100)
      WRITE(6,100)
      WRITE(6,100)
      PRINT 2001, IERRNO,NRREAD,HRUN,HEV
 2001 FORMAT(' ERROR',I4,' AT',I6,' TH EVENT. RUN, EVNT. NOS. = ',2I6)
      WRITE(6,100)
      WRITE(6,100)
      WRITE(6,100)
      IF(IERRNO.EQ.214) WRITE(6,2002)
 2002 FORMAT(1X,' ERROR 214 HAS OCCURRED. CALL ABEND.')
      IF(IERRNO.EQ.214) CALL ABEND
      IRETCD = 0
      RETURN
      END
