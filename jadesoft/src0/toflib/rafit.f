C   10/01/82 307242025  MEMBER NAME  RAFIT    (S)           FORTRAN
      SUBROUTINE FCN(NPAR,G,CHI2,X,IFLAG)
      IMPLICIT REAL*8 (A-H,P-Z)
      DIMENSION G(1),X(1)
      DIMENSION W(5),S(5),R(5),DR(5),A(5),DA(5)
      DATA W/34.4,34.2,34.5,34.6,34.7/
      DATA R/.992,.898,.985,.980,0./
      DATA DR/.044,.055,.047,.043,0./
      DATA A/-.110,-.064,-.091,-.117,-.120/
      DATA DA/.0206,.064,.0235,.0197,.0335/
C
      GO TO (10,20,30,40),IFLAG
C
   10 CONTINUE
      BCHI = 4.49E-5
      ZMZ0 = 95.
      DO   11   I=1,5
      S(I) = W(I)*W(I)
      PRINT 502, W(I),S(I),R(I),DR(I),A(I),DA(I)
  502 FORMAT('  DATA USED', 2F10.2,2F10.3,2F10.4)
   11 CONTINUE
   12 NMAX=5
C
      IBUG = 0
      X(1) = 0.
      X(2) = 1.
   20 RETURN
C
   40 CONTINUE
      IBUG = IBUG + 1
      CHI2=0.
      DO 41 I=1,NMAX
      PROP = 1./(S(I)/ZMZ0**2-1.)
      ACHI = S(I)*BCHI*PROP
      F1 = 1.+2.*X(1)*ACHI+(X(1)+X(2))**2*ACHI*ACHI
      F2 =    4.*X(2)*ACHI+8.*X(1)*X(2)*ACHI*ACHI
      RT = F1
      AE = 3./8.*F2/F1
      CHI2 = CHI2 + (AE-A(I))**2/DA(I)**2
      IF(IBUG.LE.2) PRINT 541,NPAR,S(I),PROP,ACHI
  541 FORMAT(' NPAR',I5,F10.2,2F10.5)
      IF(IBUG.LE.2) PRINT 540,I,X(1),X(2),F1,F2,RT,AE,CHI2
  540 FORMAT(' CHI2',I5,2F10.2,2F10.5,F10.3,2F12.5)
      IF(I.GT.4)  GOTO  41
      CHI2=CHI2+(RT-R(I))**2/DR(I)**2
      IF(IBUG.LE.2) PRINT 540,I,X(1),X(2),F1,F2,RT,AE,CHI2
   41 CONTINUE
C
      RETURN
C
   30 CONTINUE
C
      RETURN
      END
